    /*
     * Constants
     */
    const MAX_FRAME_TIME = 0.25;
    const DT = 1/60;

    /*
     * Interfaces
     */
    interface Updatable {
        update(dt: number): void;
    }

    interface Renderable {
        render(ctx: CanvasRenderingContext2D): void;
    }

    /*
     * Game
     */
    class Game implements Updatable, Renderable {
        updateList: Array<Updatable>;
        renderList: Array<Renderable>;

        constructor(
            updateList: Array<Updatable>,
            renderList: Array<Renderable>
        ) {
            this.updateList = updateList;
            this.renderList = renderList;
        }

        update(dt) {
            this.updateList.forEach(u => u.update(dt));
        }

        render(ctx) {
            this.renderList.forEach(r => r.render(ctx));
        }
    }

    /*
     * Tile
     */
    class Tile implements Renderable {
        static size = 24;

        id: number
        row: number;
        col: number;
        x: number;
        y: number;

        constructor(id: number, row: number, col: number) {
            this.id = id;
            this.row = row;
            this.col = col;
            this.x = col * Tile.size;
            this.y = row * Tile.size;
        }

        render(ctx) {
            if (this.id == 1) {
                ctx.strokeRect(this.x, this.y, Tile.size, Tile.size);
            }
        }
    }

    /*
     * Main Loop
     */
    let previousTime = 0;
    let accumulator = 0;
    function loop(
        currentTime: number,
        ctx: CanvasRenderingContext2D,
        game: Game
    ) {
        const frameTime = Math.min((currentTime - previousTime) / 1000,
                                   MAX_FRAME_TIME);
        accumulator += frameTime;

        while (accumulator >= DT) {
            accumulator -= DT;
            game.update(DT);
        }

        game.render(ctx);

        previousTime = currentTime;
        window.requestAnimationFrame(
            (timestamp) => loop(timestamp, ctx, game)
        );
    }

